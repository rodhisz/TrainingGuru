package com.rsz.trainingguru

fun main() {
    var a = 0

    while (true) {
        println(a)
        a++

        if (a == 10) {
            break
        }
    }

    println("------------")

    for (i in 0..10) {
        if (i % 2 == 0) {
            continue
        }
        println(i)
    }






























































}